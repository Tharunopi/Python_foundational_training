class Vechicle:
    def __init__(self, vechicleID, make, model, year, dailyRate, status, passengerCapacity, engineCapacity):
        self.vechicleID = vechicleID
        self.make = make
        self.model = model
        self.year = year
        self.dailyRate = dailyRate
        self.status = status
        self.passengerCapacity = passengerCapacity
        self.engineCapacity = engineCapacity